export const servicePagesByLocale = Object.fromEntries(
	(['et', 'ru', 'en', 'fi', 'sv'] as const).map((locale) => [
		locale,
		Object.entries(serviceSeeds).map(([key, translations]) => ({
			key,
			lang: locale,
			...translations[locale],
		})),
	]),
) as Record<Locale, ServicePageContent[]>;

export const servicePagePaths = Object.fromEntries(
	(['et', 'ru', 'en', 'fi', 'sv'] as const).map((locale) => [
		locale,
		Object.fromEntries(servicePagesByLocale[locale].map((page) => [page.key, `/${locale}/${page.slug}/`])),
	]),
) as Record<Locale, Record<string, string>>;

export const findServicePage = (locale: Locale, slug: string | undefined) =>
	servicePagesByLocale[locale]?.find((page) => page.slug === slug);
